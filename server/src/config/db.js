const { Sequelize } = require('sequelize');
require('dotenv').config();

// Create the connection instance
const sequelize = new Sequelize(
    process.env.DB_NAME,
    process.env.DB_USER,
    process.env.DB_PASS,
    {
        host: process.env.DB_HOST,
        dialect: 'postgres',
        logging: false,
        pool: {
            max: 5,
            min: 0,
            acquire: 30000,
            idle: 10000
        }
    }
);

// EXPORT THE INSTANCE DIRECTLY
module.exports = sequelize;